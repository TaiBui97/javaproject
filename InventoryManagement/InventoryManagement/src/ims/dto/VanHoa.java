package ims.dto;

/**
 *
 * @author agfor
 */
public class VanHoa {
    private String vanHoa;
    private String maVanHoa;

    public VanHoa() {
    }

    public VanHoa(String vanHoa, String maVanHoa) {
        this.vanHoa = vanHoa;
        this.maVanHoa = maVanHoa;
    }

    public String getVanHoa() {
        return vanHoa;
    }

    public void setVanHoa(String vanHoa) {
        this.vanHoa = vanHoa;
    }

    public String getMaVanHoa() {
        return maVanHoa;
    }

    public void setMaVanHoa(String maVanHoa) {
        this.maVanHoa = maVanHoa;
    }

   
    
    
            
    
}
